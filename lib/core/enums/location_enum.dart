enum Province {
  federal,
  punjab,
  sindh,
  khyberPakhtunkhwa,
  balochistan,
}

extension ProvinceX on Province {
  /// For UI
  String get toDisplayName {
    switch (this) {
      case Province.federal:
        return 'Federal Territory';
      case Province.punjab:
        return 'Punjab';
      case Province.sindh:
        return 'Sindh';
      case Province.khyberPakhtunkhwa:
        return 'Khyber Pakhtunkhwa';
      case Province.balochistan:
        return 'Balochistan';
    }
  }

  /// For APIs/URLs (kebab-case)
  String get toApiName => toDisplayName.toLowerCase().replaceAll(' ', '-');
}

