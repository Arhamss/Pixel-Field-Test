extension CityX on City {
  /// Province for this city
  Province get province => _cityToProvince[this]!;

  /// For UI (proper spacing & capitalization; keeps known tokens)
  String get toDisplayName {
    // Custom fixes for multi-word tokens & proper casing
    final special = <City, String>{
      City.deraGhaziKhan: 'Dera Ghazi Khan',
      City.deraIsmailKhan: 'Dera Ismail Khan',
      City.karorLalEsan: 'Karor Lal Esan',
      City.mandiBahauddin: 'Mandi Bahauddin',
      City.qilaDidarSingh: 'Qila Didar Singh',
      City.saiduSharif: 'Saidu Sharif',
      City.shahpurChakar: 'Shahpur Chakar',
      City.tandoAdamKhan: 'Tando Adam Khan',
      City.tandoAllahyar: 'Tando Allahyar',
      City.tandoMuhammadKhan: 'Tando Muhammad Khan',
      City.naushahroFeroze: 'Naushahro Feroze',
      City.rahimYarKhan: 'Rahim Yar Khan',
      City.wahCantonment: 'Wah Cantonment',
      City.ketiBandar: 'Keti Bandar',
      City.jhalMagsi: 'Jhal Magsi',
      City.bandaDaudShah: 'Banda Daud Shah',
      City.pishinValley: 'Pishin Valley',
      City.naushara: 'Naushara',
      City.mianChannu: 'Mian Channu',
      City.mianwaliBangla: 'Mianwali Bangla',
      City.saraiAlamgir: 'Sarai Alamgir',
    };

    if (special.containsKey(this)) return special[this]!;

    final raw = name; // camelCase
    // Insert spaces before capital letters, then title case words
    final withSpaces = raw.replaceAllMapped(
      RegExp(r'(?<=[a-z])([A-Z])'),
      (m) => ' ${m.group(1)}',
    );
    return withSpaces
        .split(' ')
        .map((w) => w.isEmpty ? w : (w[0].toUpperCase() + w.substring(1)))
        .join(' ');
  }

  /// For APIs/URLs (kebab-case), based on display name
  String get toApiName =>
      toDisplayName.toLowerCase().replaceAll(RegExp(r'\s+'), '-');
}

