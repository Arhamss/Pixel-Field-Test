/// City → Province (built once; useful for quick reverse lookup)
final Map<City, Province> _cityToProvince = () {
  final map = <City, Province>{};
  provinceToCities.forEach((prov, cities) {
    for (final c in cities) {
      map[c] = prov;
    }
  });
  return Map<City, Province>.unmodifiable(map);
}();
